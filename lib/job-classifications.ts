/**
 * 채용공고 텍스트에서 직무 분류 감지
 */
export function detectJobClassification(jobPostingText: string): JobClassification | null {
  const lowerText = jobPostingText.toLowerCase();

  for (const [classKey, classGuide] of Object.entries(JOB_CLASSIFICATIONS)) {
    for (const keyword of classGuide.keywords) {
      if (lowerText.includes(keyword.toLowerCase())) {
        return classKey as JobClassification;
      }
    }
  }

  return null;
}

/**
 * 뉴스 추출 필드에서 검색용 핵심 키워드 추출
 * 예) "신기술 도입 (AI, 클라우드 전환, MSA)" → "신기술 도입"
 *     "보안 사고·개인정보 유출"             → "보안 사고"
 */
export function extractFieldKeyword(newsField: string): string {
  // 괄호 안 부가설명 제거
  const corePhrase = newsField.replace(/\([^)]*\)/g, "").trim();
  // "·" 또는 "," 로 나열된 경우 첫 항목만 사용
  return corePhrase.split(/[·,]/)[0].trim();
}

/**
 * 직무 분류별 뉴스 검색 키워드 생성
 * 채용공고 담당업무 → 21개 분류 → 해당 분류의 뉴스 추출 필드 → "회사명 + 필드 키워드"
 */
export function generateNewsSearchKeywords(
  classification: JobClassification,
  companyName: string,
): string[] {
  const classGuide = JOB_CLASSIFICATIONS[classification];

  return classGuide.newsFields
    .map((field) => extractFieldKeyword(field))
    .filter((keyword) => keyword.length > 0)
    .map((keyword) => `${companyName} ${keyword}`);
}
